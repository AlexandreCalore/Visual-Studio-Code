let one = 1
let two = 2

// o operador '=='  é o mesmo que igual a:
console.log("IGUAL A")
console.log(one == two) // Conteúdo do one é igual ao Two?
console.log(one == 1) // Conteúdo do one é igual a '1'?
console.log(one == "1") // Conteúdo do one é igual ao texto de '1'? 
// Importante notar que, ele observa sempre o conteúdo, e não o tipo.

// o operador '!='  é o mesmo que diferente de:
console.log("DIFERENTE DE")
console.log(one != two) // Conteúdo do one é diferente do Two?
console.log(one != 1)
console.log(one != "1")