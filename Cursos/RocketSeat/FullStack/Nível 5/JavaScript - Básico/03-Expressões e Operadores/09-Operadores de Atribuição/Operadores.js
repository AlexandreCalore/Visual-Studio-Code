let value

// Operador de atribuição é o '='

value = 1 // Value RECEBE o número 1
console.log(value)

// Incremento

value += 2
console.log(value)

// Decremento

value -= 2
console.log(value)

// Multiplicar

value *= 2
console.log(value)

// Dividir

value /= 2
console.log(value)

// Resto da divisão

value %= 2
console.log(value)

// Potência

value **= 2
console.log(value)

