console.log ("SOMA: ", 12 + 2); // Soma é diferente de concatenar!
console.log ("SOMA: ", "12" + "2"); // Concatenar é a junção dos textos

console.log("SUBATRAÇÃO:", 12 - 8)

console.log("MULTIPLICAÇÃO: ", 12 * 8)

console.log("DIVISÃO: ", 12 / 8)

console.log("RESTO DA DIVISÃO: ", 13 % 2) // Ele não vai divir, apenas verificar o que restou após a divisão.

console.log("POTÊNCIA: ", 12 ** 8)