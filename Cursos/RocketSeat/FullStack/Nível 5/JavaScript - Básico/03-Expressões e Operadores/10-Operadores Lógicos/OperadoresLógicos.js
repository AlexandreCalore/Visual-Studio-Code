let email = true
let password = false //Variáveis que testou se a senha e email está correto

// Operador (E) &&

console.log(email && password) // Se email e senhas estão corretos

// Operador (OU) ||

console.log(email || password) // Se email ou senhas estão corretos

// Operador (NOT) Negação !

console.log(!email) // se ele é o oposto de verdadeiro (falso)
console.log(!password) // se ele é o oposto de verdadeiro (falso)